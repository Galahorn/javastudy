package preview01;

import java.util.Random;

public class prob12 {

	public static void main(String[] args) {

		// 중복없이 숫자를 뽑는 로또
		Random rd = new Random();

		// 로또번호를 담을 배열
		int[] lotto = new int[6];

		for (int i = 0; i < lotto.length; i++) {
			lotto[i] = rd.nextInt(45) + 1;

			for (int j = 0; j < i; j++) {
				if (lotto[i] == lotto[j]) {
					i--;
					break;

				}
			}
		}
		
		for (int i = 0; i < lotto.length; i++) {
			System.out.println("행운의 숫자 : " + lotto[i]);
			
		}

	}

}
